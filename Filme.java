public class Filme{
        
        Int id, estoque;
        String nome, DataLancamento, sinopse, ValorLocacao;

        Filme(int id, int estoque, String nome, String DataLancamento, String sinopse, double ValorLocacao){
                this.id = id;
                this.estoque = estoque;
                this.nome = nome;
                this.DataLancamento = DataLacamento;
                this.sinopse = sinopse;
                this.ValorLocacao = ValorLocacao;
        }
        
}